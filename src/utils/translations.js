// ⚡ كاش سريع للترجمات الأكثر استخداماً (ذاكرة تخزين مؤقت)
const translationCache = new Map();

// ⚡ دالة مساعدة للوصول السريع للترجمة (تستخدم الكاش)
export function getTranslation(lang, key) {
  const cacheKey = `${lang}:${key}`;
  
  if (translationCache.has(cacheKey)) {
    return translationCache.get(cacheKey);
  }
  
  const value = translations[lang]?.[key] || translations['ar']?.[key] || key;
  translationCache.set(cacheKey, value);
  return value;
}

// ⚡ قائمة المفاتيح الأكثر استخداماً للتحميل المسبق في الكاش
const mostUsedKeys = ['home', 'menu', 'about', 'gallery', 'contact', 'viewMenu', 'visitUs'];

// ⚡ تحميل مسبق للترجمات الشائعة في الكاش
mostUsedKeys.forEach(key => {
  getTranslation('ar', key);
  getTranslation('en', key);
});